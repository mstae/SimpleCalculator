using System;
using System.Collections.Generic;
using System.Text;

namespace DemoCalculatorApp
{
    public class CalculatorLogic
    {
        public double SimpleAddition(double a, double b)
        {
            return a + b;
        }
        public double SimpleSubtraction(double a, double b)
        {
            return a - b;
        }
        public double SimpleMultiplication(double a, double b)
        {
            return a * b;
        }
        public double SimpleDivision(double a, double b)
        {
            return a / b; 
        }
        public double SimpleRemainder(double a, double b)
        {
            return a % b;
        }
    }

}


